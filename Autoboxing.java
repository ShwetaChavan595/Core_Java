//In this autoboxing the primitive datatype are converted into its corrsponding wrapper class 

class Autoboxing{
public static void main(String args[])
{
int num=10;
Integer obj=num;
System.out.println("value of obj is "+obj);

double d1=29.78;
Double d2=d1;
System.out.println("value of d2 is "+d2);

char c1='e';
Character c2=c1;
System.out.println("value of c2  is "+c2);
byte b1=127;
Byte b2=b1;
System.out.println("value of b2  is "+b2);



}
}
